void solve()
{
	ll n,m,z=INF,x=0,y=0,a1,a2,a3,a4,a5,ex,che,var,k=0;
	cin>>n>>m>>x;
	//a1=min(n,m);//ans<=a1
	if(n>m)swap(n,m);
	if(x>=n)
	{
		cout<<n<<"\n";
		return;
	}
	//x<n
	z=x;
	n-=x;
	m-=x;//1,2,3

	k=m-n;
	z+=k;
	n-=k;
	m-=2*k;
	assert(n==m);//1,2,2

	if(n<0)
	{
		z+=n;
		cout<<z<<"\n";
		return;
	}
	x=n/3;
	z+=2*x;
	n-=3*x;
	m-=3*x;//1,2,2 && 1,1,2

	while(n+m>2)//left
	{
		if(n>m)swap(n,m);
		k=m-n;
		z+=k;
		if(!k)
		{
			n--;
			m-=2;
			if(n>=0&&m>=0)
			z++;
		}
		else
		{
			n-=k;
			m-=2*k;
		}
	}
	cout<<z<<"\n";

}

