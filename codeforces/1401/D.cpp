const ll N=1e5+5;
vector<vi> adj;
vi vp(N, -1);
vi b;

ll rec(int v, int p)
{
    if(adj[v].size() == 1 && v != 0)
        return vp[v] = 1;
    
    ll x = 1;
    for(auto e : adj[v])
        if(e != p)
            x += rec(e, v);

    return vp[v] = x;
}

void recc(int v, int p, int n)
{
    if(adj[v].size() == 1 && v != 0)
        return;

    for(auto e : adj[v])
        if(e != p)
        {
            b.PB(vp[e] * (n - vp[e]));
            recc(e, v, n);
        }
}

